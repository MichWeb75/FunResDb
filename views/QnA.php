                    <h3> <b> Important Note </b> </h3>
                </div>
                <div class="panel-body">
                    <b>FunResDb</b> is a curated database integrating published information on <i>A. fumigatus </i> CYP51A mutations. Although we carefully extract information from published resources we cannot guarantee that all information is correct. For this purpose, users of <b>FunResDb</b> should always check the original publications (links to PubMed are provided by <b>FunResDb</b>). Furthermore, phenotypic resistance can occur through different mechanisms. In some patient cohorts, up to 43% of  <i>A. fumigatus </i> strains resistant to azoles have been found to have wild-type CYP51A (Bueid et al., 2010). Therefore, identification of a wild-type CYP51A sequence does not automatically infer susceptibility. If you identify mistakes in <b>FunResDb</b>, we are grateful if you let us know and will try to continuously improve the database.
