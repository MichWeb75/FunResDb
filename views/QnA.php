            <div class="panel panel-default">
                <div class="panel-heading">
                    <h3 class="panel-title resist">
                        <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">
                            How can I access the FunResDb data?  </a>
                    </h3>
                </div>
                <div id="collapse4" class="panel-collapse collapse">
                    <div class="panel-body">
                        A DNA or protein sequence of <i>A. fumigatus</i> CYP51A serves as input for the database. In case of DNA sequence, the exon structure is determined by the algorithm in order translate the coding nucleotides into the corresponding amino acids. Next, the given protein sequence is compared to a wild-type sequence to detect amino acid substitutions and output their positions. The result are mutation identifiers which consist of three parts: wild-typ amino acid, position in the protein sequence, mutated amino acid (e.g. L46T; WT: leucine at position 46; mutated to tyrosine). Mutation identifiers can be searched in the database to retrieve documented <i>A. fumigatus</i> strains which contain those patterns.

                    </div> 
                </div>
            </div>


